# 6. ГЕНЕРАЦИЯ Z-КОДА
# ============================================================

def get_zcode_for(city: str, tvg_id: str) -> str | None:
    button = TVG_TO_BUTTON.get(tvg_id)
    if button is None:
        return None

    # region_code по городу — позже можно расширить
    # пока пробуем через автокоды по REGION_MAP (по названию региона)
    # но основная логика — через normalize_region_code, если в названии есть код

    # здесь можно будет добавить CITY → REGION_CODE мапу отдельно
    # пока считаем, что region_code уже нормализован извне, либо город
    # привязан к одному из стандартных кодов

    # пробуем найти shift и city_index
    shift, idx = CITY_INDEX.get(city, (None, None))
    if shift is None or idx is None:
        return None

    # здесь нужен region_code — пока заглушка: берём по shift'у
    # позже в ЧАСТИ 3 добавим CITY → REGION_CODE
    region_code = None
    # пример: если город Москва → "77", СПб → "78", Ярославль → "76" и т.д.
    # временно:
    if city == "Москва":
        region_code = "77"
    elif city in ("Санкт-Петербург", "СПб"):
        region_code = "78"
    elif city == "Ярославль":
        region_code = "76"
    # ... остальные города добавим в отдельном блоке

    if region_code is None:
        return None

    return f"Z.R({region_code}).{button}.{idx}"


