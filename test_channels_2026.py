# 7. ПАРСЕР ОДНОГО КАНАЛА
# ============================================================

SORTED_CITY_KEYS = []  # заполним после CITY_INDEX в ЧАСТИ 2

def FastParseChannel(raw_title: str, stream_url: str):
    clean = raw_title.lower()

    detected_city = None
    detected_shift = None

    for geo in SORTED_CITY_KEYS:
        if geo.lower() in clean:
            detected_city = geo
            detected_shift, _ = CITY_INDEX.get(geo, (None, None))
            break

    if detected_shift is None:
        detected_shift = 0

    tvg_id = "unknown"
    for _, (name, tvg) in FEDERAL_BUTTONS.items():
        if name.lower() in clean:
            tvg_id = tvg
            break

    zcode = None
    if detected_city and tvg_id in TVG_TO_BUTTON:
        zcode = get_zcode_for(detected_city, tvg_id)

    shift_str = f"+{detected_shift}" if detected_shift > 0 else str(detected_shift)
    extra = f' z-code="{zcode}"' if zcode else ""

    extinf = f'#EXTINF:-1 tvg-id="{tvg_id}" tvg-shift="{shift_str}"{extra},{raw_title}\n{stream_url}'
    return extinf, detected_shift


# ============================================================
# 8. ОБРАБОТКА ВСЕХ КАНАЛОВ
# ============================================================

def ProcessHugeSources(channels: list[tuple[str, str]]) -> str:
    buckets: dict[int, list[str]] = defaultdict(list)

    for title, url in channels:
        extinf, shift = FastParseChannel(title, url)
        buckets[shift].append(extinf)

    final: list[str] = []

    for shift in sorted(buckets.keys()):
        sign = f"+{shift}" if shift > 0 else str(shift)
        final.append(f"\n# --- ОРБИТА MSK {sign} ---\n")
        final.extend(buckets[shift])

    return "\n".join(final)


# ============================================================
# 9. СБОРКА ИТОГОВОГО ПЛЕЙЛИСТА
# ============================================================

def build_final_playlist() -> str:
    main_text = load_raw(MAIN_URL).strip()

    mega_channels = load_playlist_from_url(MEGA_URL)
    mega_processed = ProcessHugeSources(mega_channels).strip()

    final: list[str] = []

    if not main_text.startswith("#EXTM3U"):
        final.append("#EXTM3U")

    final.append(main_text)

    mega_lines = mega_processed.splitlines()
    mega_lines = [l for l in mega_lines if not l.startswith("#EXTM3U")]

    final.append("\n# ====== МЕГА-ИСТОЧНИК (ПОСЛЕ ULTRA-Z) ======\n")
    final.extend(mega_lines)

    return "\n".join(final)


