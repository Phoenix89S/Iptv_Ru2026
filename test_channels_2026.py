# 2. НОРМАЛИЗАЦИЯ ДУБЛЕЙ АВТОКОДОВ
# ============================================================

def normalize_region_code(code: str) -> str | None:
    """
    Дубли считаем резервом и вынимаем из них только главные цифры.
    196 → 96 → 66
    174 → 74
    102 → 02
    555 → 55
    700 → игнор
    """
    digits = "".join(ch for ch in code if ch.isdigit())
    if not digits:
        return None

    # если длина > 2 — берём последние 2 цифры
    if len(digits) > 2:
        digits = digits[-2:]

    # если такой код есть в REGION_MAP — используем
    if digits in REGION_MAP:
        return digits

    return None


